    public void agregarFila(String texto) {
        DefaultTableModel temp = (DefaultTableModel) this.tbDocumentos.getModel();
        Object nuevo[] = {""}; //esto es por las tres columnas aunque puede variar
        nuevo[0] = texto;
        temp.addRow(nuevo);
    }

